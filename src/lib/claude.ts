function extractResponseText(message: Message) {
  const parts: string[] = [];

  for (const block of message.content) {
    if (block.type === "text") {
      parts.push(block.text);
    }
  }

  return parts.join("\n").trim();
}

function extractJsonText(value: string) {
  const fencedMatch = value.match(/```(?:json)?\s*([\s\S]*?)```/i);

  if (fencedMatch?.[1]) {
    return fencedMatch[1].trim();
  }

  const firstBrace = value.indexOf("{");
  const lastBrace = value.lastIndexOf("}");

  if (firstBrace === -1 || lastBrace === -1 || lastBrace <= firstBrace) {
    throw new Error("Claude response did not contain a JSON object.");
  }

  return value.slice(firstBrace, lastBrace + 1);
}

function parseClaudeSections(rawText: string, locale: AppLocale) {
  const parsed = JSON.parse(extractJsonText(rawText)) as Record<string, unknown>;
  const sections: ReportSectionData[] = sectionFieldOrder.map((field, index) => {
    const definition = reportSectionDefinitions[index];
    const content = parsed[field];

    if (!definition) {
      throw new Error(`Missing section definition for ${field}.`);
    }

    if (typeof content !== "string" || content.trim().length < 80) {
      throw new Error(`Claude response content is too short for ${definition.title}.`);
    }

    return {
      title: definition.title,
      icon: definition.icon,
      content: content.trim(),
    };
  });

  const yearlySection = sections[5];
  const finalSection = sections[6];
  const currentYear = getCurrentYear();

  if (!yearlySection?.content.includes(String(currentYear))) {
    throw new Error("Claude yearly section is missing the current year.");
  }

  const requiredDisclaimer =
    locale === "en"
      ? "This report was generated by AI and is intended for reference only."
      : "본 리포트는 AI가 생성한 참고용 콘텐츠입니다.";

  if (!finalSection?.content.includes(requiredDisclaimer)) {
    throw new Error("Claude report is missing the required disclaimer.");
  }

  return sections;
}

async function delay(milliseconds: number) {
  await new Promise((resolve) => {
    setTimeout(resolve, milliseconds);
  });
}

export function isClaudeConfigured() {
  return Boolean(getAnthropicApiKey());
}

export async function generateClaudeReport(
  input: BirthInput,
  sajuData: SajuData,
  locale: AppLocale = defaultLocale,
): Promise<ReportSectionData[] | null> {
  if (!isClaudeConfigured()) {
    return null;
  }

  const client = getAnthropicClient();
  const system = buildSystemPrompt(locale);
  const userPrompt = buildUserPrompt(input, sajuData, locale);

  for (let retry = 0; retry <= anthropicRetryCount; retry += 1) {
    try {
      const message = await client.messages.create({
        model: anthropicModel,
        max_tokens: anthropicMaxTokens,
        stream: false,
        system,
        messages: [
          {
            role: "user",
            content: userPrompt,
          },
        ],
        output_config: {
          format: {
            type: "json_schema",
            schema: buildStructuredOutputSchema(locale),
          },
        },
      });

      if (message.stop_reason === "max_tokens") {
        throw new Error("Claude response hit max_tokens before completing structured output.");
      }

      if (message.stop_reason === "refusal") {
        throw new Error("Claude refused the report generation request.");
      }

      return parseClaudeSections(extractResponseText(message), locale);
    } catch (error) {
      if (retry === anthropicRetryCount) {
        throw error;
      }

      await delay(anthropicRetryDelayMs);
    }
  }

  return null;
}
