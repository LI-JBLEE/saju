import Anthropic from "@anthropic-ai/sdk";
import type { Message } from "@anthropic-ai/sdk/resources/messages/messages";
import { defaultLocale, localizeOhaengLabel, type AppLocale } from "@/lib/i18n";
import { reportSectionDefinitions } from "@/lib/report-builder";
import type { BirthInput, SajuData } from "@/lib/saju/types";
import type { ReportSectionData } from "@/types";

const anthropicModel = "claude-sonnet-4-6";
const anthropicTimeoutMs = 60_000;
const anthropicRetryCount = 2;
const anthropicRetryDelayMs = 1_000;
const anthropicMaxTokens = 4_096;
const sectionFieldOrder = [
  "basic_info",
  "personality",
  "career",
  "romance",
  "health",
  "yearly_fortune",
  "advice",
] as const;

let anthropicClient: Anthropic | null = null;

function getCurrentYear() {
  return new Date().getFullYear();
}

function formatBirthInput(input: BirthInput, locale: AppLocale) {
  if (locale === "en") {
    return [
      `- Birth year: ${input.birthYear}`,
      `- Birth month: ${input.birthMonth}`,
      `- Birth day: ${input.birthDay}`,
      `- Birth hour: ${input.birthHour === null ? "Unknown" : `${input.birthHour}:00`}`,
      `- Gender: ${input.gender === "female" ? "Female" : "Male"}`,
    ].join("\n");
  }

  return [
    `- 태어난 연도: ${input.birthYear}`,
    `- 태어난 월: ${input.birthMonth}`,
    `- 태어난 일: ${input.birthDay}`,
    `- 태어난 시간: ${input.birthHour === null ? "모름" : `${input.birthHour}시`}`,
    `- 성별: ${input.gender === "female" ? "여성" : "남성"}`,
  ].join("\n");
}

function formatPillar(name: string, pillar: SajuData["pillars"]["year"]) {
  return `- ${name}: ${pillar.cheongan}${pillar.jiji} (${pillar.ohaeng})`;
}

function formatSajuData(sajuData: SajuData, locale: AppLocale) {
  const lines =
    locale === "en"
      ? [
          `- Year pillar: ${sajuData.pillars.year.cheongan}${sajuData.pillars.year.jiji} (${localizeOhaengLabel(sajuData.pillars.year.ohaeng, locale)})`,
          `- Month pillar: ${sajuData.pillars.month.cheongan}${sajuData.pillars.month.jiji} (${localizeOhaengLabel(sajuData.pillars.month.ohaeng, locale)})`,
          `- Day pillar: ${sajuData.pillars.day.cheongan}${sajuData.pillars.day.jiji} (${localizeOhaengLabel(sajuData.pillars.day.ohaeng, locale)})`,
        ]
      : [
          formatPillar("년주", sajuData.pillars.year),
          formatPillar("월주", sajuData.pillars.month),
          formatPillar("일주", sajuData.pillars.day),
        ];

  if (sajuData.pillars.hour) {
    lines.push(
      locale === "en"
        ? `- Hour pillar: ${sajuData.pillars.hour.cheongan}${sajuData.pillars.hour.jiji} (${localizeOhaengLabel(sajuData.pillars.hour.ohaeng, locale)})`
        : formatPillar("시주", sajuData.pillars.hour),
    );
  } else {
    lines.push(
      locale === "en" ? "- Hour pillar: omitted because the birth hour is unknown" : "- 시주: 없음 (태어난 시간을 모름)",
    );
  }

  if (locale === "en") {
    lines.push(
      `- Five-element balance: Wood ${sajuData.ohaengRatio.목}, Fire ${sajuData.ohaengRatio.화}, Earth ${sajuData.ohaengRatio.토}, Metal ${sajuData.ohaengRatio.금}, Water ${sajuData.ohaengRatio.수}`,
    );
    lines.push(`- Day master: ${sajuData.ilgan}`);
    lines.push(`- Supportive element: ${localizeOhaengLabel(sajuData.yongsin, locale)}`);
    lines.push(`- Challenging element: ${localizeOhaengLabel(sajuData.gisin, locale)}`);
  } else {
    lines.push(
      `- 오행 비율: 목 ${sajuData.ohaengRatio.목}, 화 ${sajuData.ohaengRatio.화}, 토 ${sajuData.ohaengRatio.토}, 금 ${sajuData.ohaengRatio.금}, 수 ${sajuData.ohaengRatio.수}`,
    );
    lines.push(`- 일간: ${sajuData.ilgan}`);
    lines.push(`- 용신: ${sajuData.yongsin}`);
    lines.push(`- 기신: ${sajuData.gisin}`);
  }

  return lines.join("\n");
}

function buildSystemPrompt(locale: AppLocale) {
  const sectionSchema = reportSectionDefinitions
    .map(
      (section, index) =>
        locale === "en"
          ? `${index + 1}. Write the "${section.title}" content in natural English with at least 220 characters.`
          : `${index + 1}. "${section.title}" 내용은 220자 이상 한국어 문단으로 작성`,
    )
    .join("\n");

  if (locale === "en") {
    return [
      "You are an expert editor for modern English saju reports.",
      "The output format is enforced by the API structured output schema, so return values only without code fences or prefaces.",
      sectionSchema,
      "The sixth section must include the current year as digits.",
      "If the birth hour is unknown, avoid overconfident statements about the hour pillar.",
      "The last sentence of the seventh section must be exactly 'This report was generated by AI and is intended for reference only.'",
      "Avoid deterministic fate claims, medical or legal certainty, and fear-based language.",
      "Keep the tone warm, specific, and grounded rather than mystical or exaggerated.",
    ].join("\n");
  }

  return [
    "너는 한국어 사주 리포트를 작성하는 전문 에디터다.",
    "출력 형식은 API의 structured output schema로 강제되므로 코드펜스, JSON 설명, 서문 없이 값만 채운다.",
    sectionSchema,
    "6번째 '올해 운세' 섹션에는 반드시 현재 연도를 숫자로 포함한다.",
    "태어난 시간이 없으면 시주 단정 표현을 쓰지 말고 조심스럽게 설명한다.",
    "7번째 '종합 조언' 섹션 마지막 문장은 정확히 '본 리포트는 AI가 생성한 참고용 콘텐츠입니다.' 로 끝낸다.",
    "운명을 단정하는 표현, 의료/법률 확정 표현, 과도한 공포 조장은 금지한다.",
    "문체는 따뜻하고 구체적이며 과장되지 않게 유지한다.",
  ].join("\n");
}

